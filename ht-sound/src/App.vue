<template>
  <main>
    <div class="mt-5 container">
      <div class="header">
        <!-- open modale  -->
        <div v-if="!isModaleOpen" class="btn-container">
          <button class="open-modale" @click="openModale()">
            <img src="@/assets/menu.svg" alt="close" class="open-modale">
          </button>
        </div>

        <!-- modale -->
        <transition appear mode="in-out" name="slide">
          <Modale v-if="isModaleOpen" @closeModale="closeModale()">
            <h2> Choissiez votre palette de couleurs</h2>
            <Color
              v-for="theme in themes"
              :theme="theme"
              @changeColors="changeColors($event)"
              :key="theme.color"
            ></Color>
          </Modale>
        </transition>
      </div>

      <!-- PARTITION -->
      <div class="music">
        <div>
          <draggable
            class="draggable-list"
            :list="sheet"
            :group="{ name: 'myGroup', put: true }"
          >
            <div
              class="list-item"
              :style="
                element.color !== '' ? 'background-color:' + element.color : ''
              "
              v-for="element in sheet"
              :key="element.name"
            >
              {{ element.name }}
            </div>
          </draggable>
        </div>
      </div>

      <!-- BUTTONS -->
      <div class="play flex-center">
        <button @click="playSound()">Play</button>
        <button @click="stopSound()">Stop</button>
      </div>

      <!-- NOTES DISPONILBES -->
      <div class="notes flex-center">
        <div>
          <draggable
            class="draggable-list"
            :list="availableNotes"
            :group="{ name: 'myGroup', pull: 'clone', put: false }"
          >
            <div
              class="list-item"
              :style="
                element.color !== '' ? 'background-color:' + element.color : ''
              "
              v-for="element in availableNotes"
              :key="element.name"
            >
              {{ element.name }}
            </div>
          </draggable>
        </div>

        <div class="trash">
          <draggable
            class="draggable-list trashList"
            :list="trash"
            :group="{ name: 'myGroup', pull: 'clone', put: true }"
          >
          </draggable>
        </div>
      </div>
    </div>
  </main>
</template>
<script>
import draggable from "vuedraggable";
import Color from "./components/Color.vue";
import Modale from "./components/Modale.vue";
import * as Tone from "tone";

export default {
  components: {
    draggable,
    Color,
    Modale,
  },
  data() {
    return {
      synth: null,
      sequence: null,
      loopNumber: 1,
      tempo: 0.5,
      sheet: [
        {name:"C4",color:"#ee74e1",},
        {name:"C4",color:"#ee74e1",},
        {name:"C4",color:"#ee74e1",},
        {name:"D4",color:"#b197ff",},
        {name:"E4",color:"#2bb6ff",},
        {name:"D4",color:"#b197ff",},
        {name:"C4",color:"#ee74e1",},
        {name:"E4",color:"#2bb6ff",},
        {name:"D4",color:"#b197ff",},
        {name:"D4",color:"#b197ff",},
        {name:"C4",color:"#ee74e1",}
      ],
      trash: [],
      availableNotes: [
        { name: "C4", color: "#ff6d93" },
        { name: "D4", color: "#ef5b7a" },
        { name: "E4", color: "#dd4a61" },
        { name: "F4", color: "#cb3949" },
        { name: "G4", color: "#b72832" },
        { name: "A4", color: "#a3161b" },
        { name: "B4", color: "#8e0000" },
      ],
      effects: ["distortion", "bitCrusher", "chorus", "chebyshev", "none"],
      activeEffect: "none",
      instruments: ["fmSynth", "amSynth", "synth"],
      activeInstrument: "synth",
      activeTheme: "green",
      themes: [
        { colorFrom: "#85FFBD", colorTo: "#FFFB7D", label: "VERT" },
        { colorFrom: "#EE74E1", colorTo: "#3EECAC", label: "ROSE" },
        { colorFrom: "ff6d93", colorTo: "8e0000", label: "MARRON" },
        { colorFrom: "#9bc5c3", colorTo: "#6d66ca", label: "BLEU" },
        { colorFrom: "#fafa5e", colorTo: "#2A4858", label: "VIOLET-BLEU" },
      ],
      gradient: [
        "#ff6d93",
        "#ef5b7a",
        "#dd4a61",
        "#cb3949",
        "#b72832",
        "#a3161b",
        "#8e0000",
      ],
      isModaleOpen: false,
    };
  },
  methods: {
    playSound() {
      Tone.Transport.stop();
      Tone.Transport.clear();
      if(this.sequence) this.sequence.dispose();

      // if (this.sequence) this.sequence.removeAll();
      // if (this.synth) this.synth.dispose();
      let effect;
      // const now = Tone.now();
      // const seq = ["E4", "D#4", "E4", "D#4", "E4", "B3", "D4", "C4", "A3"];
      // INSTRUMENT
      // if (this.instrument === 'fmSynth') synth = new Tone.FMSynth();
      // else if (this.instrument === 'amSynth') synth = new Tone.AMSynth();
      // else if (this.instrument === 'synth') synth = new Tone.Synth();
      this.synth = new Tone.Synth();

      // EFFET
      if (this.activeEffect !== "none") {
        if (this.activeEffect === "distortion")
          effect = new Tone.Distortion(1).toDestination();
        if (this.activeEffect === "bitCrusher")
          effect = new Tone.BitCrusher(3).toDestination();
        if (this.activeEffect === "chorus")
          effect = new Tone.Chorus(4, 2.5, 0.5).toDestination().start();
        if (this.activeEffect === "chebyshev")
          effect = new Tone.Chebyshev(50).toDestination();

        this.synth = this.synth.connect(effect);
      } else this.synth = this.synth.toDestination();

      this.sequence = new Tone.Sequence((time, note) => {
         this.synth.triggerAttackRelease(note.name, "8t", time);        
      }, this.sheet);

      this.sequence.loop = this.loopNumber;
      this.sequence.playbackRate = 1;
      this.sequence.start(0);
      Tone.Transport.start();
    },
    stopSound() {
      Tone.Transport.stop();
      Tone.Transport.clear();
      this.sequence.dispose();
    },
    changeColors(colors) {
      this.gradient = colors;
      this.availableNotes.forEach((note, i) => (note.color = this.gradient[i]));

      // change les notes déjà entrées
      this.availableNotes.map((exempleNote) => {
        this.sheet.filter((sheetNote) =>
          sheetNote.name === exempleNote.name
            ? (sheetNote.color = exempleNote.color)
            : null
        );
      });

      // ferme la modale
      this.closeModale();
    },
    closeModale() {
      this.isModaleOpen = false;
    },
    openModale() {
      this.isModaleOpen = true;
    },
  },
};
</script>
